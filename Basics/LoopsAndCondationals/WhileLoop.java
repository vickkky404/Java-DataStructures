package Basics.LoopsAndCondationals;

public class WhileLoop {
    public static void main(String[] args) {
       /*
       syntax:-


         while(condition){
          //body of loop
         }

       */

         int num = 1;
         while (num <= 10) {
             System.out.println(num);
             num++;
         }

         
     }
 }